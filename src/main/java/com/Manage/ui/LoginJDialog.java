  void checkTaiKhoan(){
       String username = txtUsername.getText();
       String password = txtPass.getText();
       TaiKhoan tk = dao.selectByUsername(username);
       if(tk == null){
           MsgBox.alert(this, "Bạn chưa điền thông tin!");
       } else {
           if(!username.equalsIgnoreCase(txtUsername.getText())){
               MsgBox.alert(this, "Sai tên đăng nhập !");
           }
           if(!password.equals(txtPass.getText())){
              MsgBox.alert(this, "Sai mật khẩu !");
           } else {
              Auth.user = tk;
//              String tenDangNhap = Auth.user.getUsername();
//              label.value = tenDangNhap;
               JOptionPane.showMessageDialog(null, "Đăng nhập thành công!");
               this.dispose(); //dong tab 
               new GioHangJDialog().setVisible(true);
               //new ThongTinNhanVen().setVisible(true);
//               if(tk.getVaitro().equalsIgnoreCase("admin")){
//                   new HomeJDialog(null, true).setVisible(true);
//               } else {
//                    new HomeKHangJDialog(null, true).setVisible(true);
//                   //new HomeNewJPanel(null, true).setVisible(true);
//               }
           }
       }  
   }
  
